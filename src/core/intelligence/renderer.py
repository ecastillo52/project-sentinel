# core/intelligence/renderer.py

"""
Project Sentinel

Intelligence Renderer

Responsible for displaying historical analysis generated by
Sentinel Intelligence.

This module performs no analysis.

It only renders intelligence reports.
"""

# ==========================================================
# Constants
# ==========================================================

REPORT_WIDTH = 52
LABEL_WIDTH = 24

LINE = "=" * REPORT_WIDTH


# ==========================================================
# Public API
# ==========================================================

def render(report: dict) -> None:
    """
    Render a complete intelligence report.
    """

    print_header()

    if "history" in report:
        print_history(report["history"])

    if "performance" in report:
        print_performance(report["performance"])

    if "cpu" in report:
        print_cpu(report["cpu"])

    if "gpu" in report:
        print_gpu(report["gpu"])

    if "memory" in report:
        print_memory(report["memory"])

    if "recommendations" in report:
        print_recommendations(report["recommendations"])


# ==========================================================
# Header
# ==========================================================

def print_header() -> None:
    """
    Print report header.
    """

    divider()

    print("SENTINEL INTELLIGENCE".center(REPORT_WIDTH))

    divider()
    blank()


# ==========================================================
# History
# ==========================================================

def print_history(history: dict) -> None:
    """
    Render historical summary.
    """

    heading("History")

    field("Total Sessions", history.get("total_sessions"))
    field("Games Played", history.get("games"))
    field("First Session", history.get("first_session"))
    field("Latest Session", history.get("latest_session"))

    blank()


# ==========================================================
# Performance
# ==========================================================

def print_performance(performance: dict) -> None:
    """
    Render performance statistics.
    """

    heading("Performance")

    field("Average FPS", performance.get("average_fps"))
    field("Best FPS", performance.get("best_fps"))
    field("Trend", performance.get("trend"))

    blank()


# ==========================================================
# CPU
# ==========================================================

def print_cpu(cpu: dict) -> None:
    """
    Render CPU statistics.
    """

    heading("CPU")

    field("Average Temp", cpu.get("average_temp"))
    field("Highest Temp", cpu.get("highest_temp"))
    field("Trend", cpu.get("trend"))

    blank()


# ==========================================================
# GPU
# ==========================================================

def print_gpu(gpu: dict) -> None:
    """
    Render GPU statistics.
    """

    heading("GPU")

    field("Average Temp", gpu.get("average_temp"))
    field("Highest Temp", gpu.get("highest_temp"))
    field("Trend", gpu.get("trend"))

    blank()


# ==========================================================
# Memory
# ==========================================================

def print_memory(memory: dict) -> None:
    """
    Render memory statistics.
    """

    heading("Memory")

    field("Average Load", memory.get("average_load"))
    field("Highest Load", memory.get("highest_load"))
    field("Trend", memory.get("trend"))

    blank()


# ==========================================================
# Recommendations
# ==========================================================

def print_recommendations(recommendations: list[str]) -> None:
    """
    Render Sentinel recommendations.
    """

    heading("Recommendations")

    if not recommendations:

        print("No recommendations.")

    else:

        for recommendation in recommendations:
            print(f"• {recommendation}")

    blank()


# ==========================================================
# Helpers
# ==========================================================

def heading(title: str) -> None:
    """
    Print a section heading.
    """

    divider()

    print(title)

    divider()

    blank()


def field(label: str, value) -> None:
    """
    Print an aligned label/value pair.
    """

    if value is None:
        value = "--"

    print(f"{label:<{LABEL_WIDTH}} {value}")


def divider() -> None:
    """
    Print divider.
    """

    print(LINE)


def blank() -> None:
    """
    Print blank line.
    """

    print()